@lru_cache(maxsize=1)
def _rule_overrides() -> dict[str, RuleContract]:
    overrides = dict(_PY_RULE_CONTRACTS)
    overrides.update(_build_js_pattern_contracts())
    overrides.update(_JS_HEURISTIC_RULE_CONTRACTS)
    overrides.update(_GO_RULE_CONTRACTS)
    overrides.update(_JAVA_RULE_CONTRACTS)
    overrides.update(_CSHARP_RULE_CONTRACTS)
    return overrides


def _language_tuple(language: str | None) -> tuple[str, ...]:
    if not language:
        return tuple()
    normalized = language.strip().lower()
    if normalized == "python":
        return ("python",)
    if normalized in {"javascript", "typescript", "js", "ts"}:
        return ("javascript", "typescript")
    if normalized == "go":
        return ("go",)
    if normalized == "java":
        return ("java",)
    if normalized in {"csharp", "cs", "c#"}:
        return ("csharp",)
    return (normalized,)


def _prefix_languages(rule_id: str) -> tuple[str, ...]:
    prefix = rule_id.split("-", 1)[0]
    return {
        "PY": ("python",),
        "JS": ("javascript", "typescript"),
        "GO": ("go",),
        "JV": ("java",),
        "CS": ("csharp",),
    }.get(prefix, tuple())


def _placeholder_contract(rule_id: str) -> RuleContract:
    languages = _prefix_languages(rule_id)
    prefix = languages[0] if languages else "unknown"
    return RuleContract(
        rule_id=rule_id,
        title=f"Undocumented {prefix} detector {rule_id}",
        category="security",
        default_severity="medium",
        languages=languages,
        maturity="beta",
        precision="medium",
        summary="This detector exists in the analyzer, but its contract has not been manually curated yet.",
        remediation="Review the emitted finding details and add a curated contract once the detector behavior is stable.",
        docs_url=_COVERAGE_DOC,
        known_limitations=(
            "This placeholder contract should be replaced with rule-specific metadata as the catalog matures.",
        ),
        tags=("catalog-gap", prefix),
    )


def _build_from_cwe_contract(
    cwe_token: str,
    token: str,
    title: str,
    category: str,
    severity: str,
    language: str | None,
) -> RuleContract:
    """Build a contract from a curated CWE base, optionally overlaying token-specific metadata."""
    base = _CWE_CONTRACTS[cwe_token]
    if not token:
        return base
    inferred_languages = base.languages if not language else _language_tuple(language)
    return RuleContract(
        rule_id=token,
        cwe=base.cwe,
        title=title or base.title,
        category=category or base.category,
        default_severity=severity or base.default_severity,
        languages=inferred_languages,
        maturity=base.maturity,
        precision=base.precision,
        summary=base.summary,
        remediation=base.remediation,
        docs_url=base.docs_url,
        known_limitations=base.known_limitations,
        tags=base.tags,
    )


